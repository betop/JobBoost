#!/usr/bin/env node

/**
 * Generate Base64 font data and inject into pdfGenerator.js
 * This script reads TTF files and creates base64 data assignments
 */

const fs = require('fs');
const path = require('path');

const fontMap = {
  'Montserrat-Bold.ttf': './fonts/Montserrat/static/Montserrat-Bold.ttf',
  'Montserrat-Regular.ttf': './fonts/Montserrat/static/Montserrat-Regular.ttf',
  'Lato-Regular.ttf': './fonts/Lato/Lato-Regular.ttf',
  'Lato-Bold.ttf': './fonts/Lato/Lato-Bold.ttf',
  'Roboto-Regular.ttf': './fonts/Roboto/static/Roboto-Regular.ttf',
  'Roboto-Bold.ttf': './fonts/Roboto/static/Roboto-Bold.ttf',
  'Poppins-Regular.ttf': './fonts/Poppins/Poppins-Regular.ttf',
  'Poppins-Bold.ttf': './fonts/Poppins/Poppins-Bold.ttf',
  'Inter-Regular.ttf': './fonts/Inter/static/Inter_18pt-Regular.ttf',
  'Inter-Bold.ttf': './fonts/Inter/static/Inter_18pt-Bold.ttf',
  'SourceSans3-Regular.ttf': './fonts/Source_Sans_3/static/SourceSans3-Regular.ttf',
  'SourceSans3-Bold.ttf': './fonts/Source_Sans_3/static/SourceSans3-Bold.ttf',
};

const varNameMap = {
  'Montserrat-Bold.ttf': 'montserratBoldFont',
  'Montserrat-Regular.ttf': 'montserratRegularFont',
  'Lato-Regular.ttf': 'latoRegularFont',
  'Lato-Bold.ttf': 'latoBoldFont',
  'Roboto-Regular.ttf': 'robotoRegularFont',
  'Roboto-Bold.ttf': 'robotoBoldFont',
  'Poppins-Regular.ttf': 'poppinsRegularFont',
  'Poppins-Bold.ttf': 'poppinsBoldFont',
  'Inter-Regular.ttf': 'interRegularFont',
  'Inter-Bold.ttf': 'interBoldFont',
  'SourceSans3-Regular.ttf': 'sourceSans3RegularFont',
  'SourceSans3-Bold.ttf': 'sourceSans3BoldFont',
};

// Generate base64 data for each font
const fontData = {};
for (const [displayName, filePath] of Object.entries(fontMap)) {
  try {
    const fullPath = path.join(__dirname, filePath);
    if (!fs.existsSync(fullPath)) {
      console.warn(`⚠️  File not found: ${fullPath}`);
      continue;
    }
    
    const buffer = fs.readFileSync(fullPath);
    const base64 = buffer.toString('base64');
    fontData[displayName] = base64;
    console.log(`✓ Loaded: ${displayName} (${(buffer.length / 1024).toFixed(1)}KB)`);
  } catch (e) {
    console.error(`✗ Error loading ${filePath}:`, e.message);
  }
}

// Generate JavaScript code to assign to window/self
let jsCode = '// Auto-generated font data - DO NOT EDIT\n';
jsCode += '// Generated by generate-font-data.js\n\n';
jsCode += '(function() {\n';
jsCode += '  const ctx = typeof window !== "undefined" ? window : self;\n\n';

for (const [displayName, varName] of Object.entries(varNameMap)) {
  if (fontData[displayName]) {
    jsCode += `  ctx.${varName} = "${fontData[displayName]}";\n`;
  }
}

jsCode += '})();\n';

// Write to resume_fonts.js
const outputPath = path.join(__dirname, 'resume_fonts.js');
fs.writeFileSync(outputPath, jsCode);

console.log(`\n✅ Generated: ${outputPath}`);
console.log(`   Total fonts: ${Object.keys(fontData).length}/12`);

// Also create a test file
const testCode = `// Test that fonts are loaded
const ctx = typeof window !== 'undefined' ? window : self;

console.log('Testing font data availability...');
const expectedFonts = [
  'montserratBoldFont',
  'montserratRegularFont',
  'latoRegularFont',
  'latoBoldFont',
  'robotoRegularFont',
  'robotoBoldFont',
  'poppinsRegularFont',
  'poppinsBoldFont',
  'interRegularFont',
  'interBoldFont',
  'sourceSans3RegularFont',
  'sourceSans3BoldFont',
];

let loaded = 0;
let missing = 0;

expectedFonts.forEach(font => {
  if (ctx[font]) {
    console.log(\`✓ \${font}\`);
    loaded++;
  } else {
    console.log(\`✗ \${font} - MISSING\`);
    missing++;
  }
});

console.log(\`\\nResult: \${loaded}/12 fonts loaded\`);
if (missing > 0) {
  console.error(\`ERROR: \${missing} fonts missing!\`);
}
`;

const testPath = path.join(__dirname, 'test-fonts-loaded.js');
fs.writeFileSync(testPath, testCode);
console.log(`✅ Created test file: ${testPath}`);
